            Are your animals sick?
            <i class="icon ion-md-add"></i>
        <i class="icon ion-md-remove"></i>
    </a>
    <div class="answer">
        <p>
        No, the shelter receives the animals in different conditions. Some of them are completely healthy and were living in a home where they received full care, others had certain injuries or illnesses. The shelter provides any necessary medical care and will not put the animals up for adoption unless they are fully healthy.

        In the case of simple diseases that will not interfere with the adoption process (like fungus), that will be declared to the adopter as long as he/she will continue the treatment in their home in line with directions from the shelter’s veterinary, also you will be able to follow up with the vet until the end of the treatment. In this case, the adopter has helped an unwanted pet, which a lot of people might not adopt, and is in need of more care than the others.</p>
